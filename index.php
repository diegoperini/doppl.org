    <p>Doppl is a new programming language that aims providing a natural syntax for implementing parallel algorithms,<br />
    designing data structures for shared memory applications and automated message passing among multiple tasks.<br />
    The name is an abbreviation of `data oriented parallel programming language`.</p>
    <p>Doppl development is logged as diary entries which extend, improve or remove feautes that are introduced by<br />
    previous ones. Diary entries are called iterations and numbered by their versions. Once the language standard becomes<br />
    mature enough and the compiler is relatively stable, this site will replace itself with a proper web app which will<br />
    include the language standard(completely rewritten, a structured paper), a friendly documentation, a bug tracker,<br />
    a mail group, the project source, milestones, a discussion platform and a blog.</p>
